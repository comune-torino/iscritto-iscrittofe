<h2 class="no-bottom">Informazioni per scelta nidi</h2>
<div class="form-step">
	<p>Importanti informazioni per la scelta delle strutture in preferenza:</p>
	<ul>
		<li>nella domanda possono essere espresse, in ordine, al massimo 10 preferenze: ogni preferenza indica il nido o una sezione primavera e il tempo di frequenza richiesto. Per tutti i nidi &egrave; possibile scegliere il tempo lungo (7:30-17:30), per alcuni nidi il tempo breve (7:30-13:30), per le sezioni primavera &egrave; possibile scegliere solo il tempo lungo (l'orario &egrave definito da ciascun gestore).</li>
		<li>le domande di iscrizione che indicano condizioni di priorit&agrave; per disabilit&agrave;, disagio sociale o gravi problemi di salute del bambino o della bambina devono indicare almeno 3 preferenze (art. 5 bis del Regolamento n. 231)</li>
		<li>se viene assegnato il posto nella prima preferenza, la rinuncia comporta la cancellazione dalla graduatoria cittadina</li>
		<li>se il posto viene assegnato in una preferenza che non &egrave; la prima scelta, si pu&ograve; rinunciare e restare in lista d'attesa per due volte; la terza rinuncia comporta la cancellazione dalla graduatoria cittadina</li>
		<li>l'accettazione del posto comporta la cancellazione dalla graduatoria cittadina</li>
	</ul>
	<p><strong>In considerazione delle regole inerenti la rinuncia, &egrave; importante indicare solo le strutture alle quali la famiglia &egrave; realmente interessata</strong>.</p>
